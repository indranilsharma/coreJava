package com.allKeyword.Static.Variable;

public class Manager1 {

	static int i;
	static double j;
	static boolean b;
	static String k;
	static float m;
	static char c;

	public static void main(String[] args) {
		System.out.println(i);//// default value JVM provide int 0
		System.out.println(j);// default value JVM provide double 0.0
		System.out.println(b);// default value JVM provide boolean false
		System.out.println(k);// default value JVM provide String null
		System.out.println(m);// default value JVM provide float 0.0
		System.out.println(c);// default value JVM provide char []
	}
}
